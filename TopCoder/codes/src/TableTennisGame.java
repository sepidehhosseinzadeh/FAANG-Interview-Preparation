    public int[] finalScore(int A, int B) {
        if (!(A + B >= 11 && Math.abs(A - B) <= 2 && A >= 2 && B >= 2)) return new int[]{};
        if (A == B) return new int[]{A + 1, A - 1};
        if(Math.abs(11-(A+B-11)) < 2) return new int[]{};
        return new int[]{Math.max(11, A + B - 11), Math.min(11, A + B - 11)};
    }

