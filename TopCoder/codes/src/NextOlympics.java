    public int countDays(String today) 
    {
        today = today.replace(".","-");
        LocalDate d1 = LocalDate.parse("2021-07-23", DateTimeFormatter.ISO_LOCAL_DATE);
        LocalDate d2 = LocalDate.parse(today, DateTimeFormatter.ISO_LOCAL_DATE);
        Duration diff = Duration.between(d2.atStartOfDay(), d1.atStartOfDay());
        int diffDays = (int)diff.toDays();
        return diffDays;
    }

