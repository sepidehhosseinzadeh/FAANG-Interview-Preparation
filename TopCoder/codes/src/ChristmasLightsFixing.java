    public int[] fixingTime(int N, long step)
    {
        String bin = Long.toBinaryString(step);
        int[] res = new int[Long.bitCount(step)];
        for(int i = bin.length()-1, j = 0; i >= 0; i--)
            if(bin.charAt(i) == '1')
                res[j++] = i+1;

        return res;
    }

